package DTO;

public abstract class BaseDTO {
    protected int id;
    protected int status;

    public BaseDTO() {
    }

    public BaseDTO(int id, int status) {
        this.id = id;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
